package x.android.xml;

/**
 * \defgroup x_android_xml XML Support
 * This package has some classes that support simple XML loading and writing.
 * The support of this class is basic. No namespaces, data areas nor tag
 * values are supported. Only tags and their respective attributes and
 * comments, that are completely skiped. These classes are maint only to
 * support the format of resource files in XML.
 * - \b CXmlFile: Represents a file in this base XML format.
 * - \b CXmlNode: An XML element node.
 * - \b CXmlAttr: A node attribute, name and value.
 * - \b CXmlTok: Parses and validates an XML file.
 * .
 * @{ *//* ---------------------------------------------------------------- */
///@} x_android_xml
