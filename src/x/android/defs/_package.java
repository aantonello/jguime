package x.android.defs;

/**
 * \defgroup x_android_defs General Constants
 * This package has constants used in general available.
 * The \c ERROR class has all general error constants. Notice that all error
 * codes are negative numbers. Error descriptions are available in Brazilian
 * Portuguese and US English languages. They can be recovered by using the \c
 * res.getErrorDesc() function.
 *
 * The \b ENC class has constants for string encoding conversion. It is used
 * extensively when converting text to binary and vice-versa.
 *
 * The \b ALIGN class is used for square alignment and calculations inside UI
 * elements.
 *
 * The \b RES class has constants for things in the resources area. It has
 * identifiers of strings in general. Used in the UI and \c
 * x.android.utils.time_t class.
 *
 * In this group you will find:
 * - x.android.utils#ERROR: Lists all errors constants.
 * - x.android.utils#ENC: List encoding constants, enabling conversion from
 *   one to another character type set.
 * - x.android.utils#ALIGN: Alignment constants. Although this is used mostly
 *   in the *x.android.ui* package is available here for all purposes.
 * - x.android.utils#RES: Resources constants. Enable one to recover any
 *   resource available in this library.
 * .
 * @{ *//* ---------------------------------------------------------------- */
///@} x.android.defs
